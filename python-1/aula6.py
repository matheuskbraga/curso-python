# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1')), sep=' || ')
print(type(float('1') + 1))
print((int('1') + 1))
print((float('1') + 1))
print(bool(' ')) # Se tem algo dentro das aspas simples até mesmo um SPACE então é TRUE
print(bool(''))  # Se não tem algo dentro das aspas simples até mesmo um SPACE então é FALSE
print(str(11) + 'b')

